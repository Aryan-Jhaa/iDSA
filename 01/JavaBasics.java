public class JavaBasics {
    public static void main(String args[]){
        System.out.println("Hello Java");

        // Variables 
        int a = 10;
        System.out.println(a);

        byte b = 8;
        System.out.println(b);

        char c = 'c';
        System.out.println(c);

        boolean var = false;
        System.out.println(var);

        float price = 10.5f;
        System.out.println(price);

    }
}

